// Dynamic Typing:
// - Variable type is decided at runtime.
// - No need to specify a data type (int, string, etc.).
// - The same variable can store different data types.
// - JavaScript automatically detects the type from the assigned value.
// - That's why JavaScript is called a dynamically typed language.

let data = 10;        // Number
data = "Hello";       // String
data = true;          // Boolean

console.log(data);    // true

console.log(typeof data)



