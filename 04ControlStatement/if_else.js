// if else if else statement
// Used to execute different blocks of code based on conditions

let marks = 85;

if (marks >= 90) {
    console.log("Grade A+");
}
else if (marks >= 75) {
    console.log("Grade A");
}
else if (marks >= 60) {
    console.log("Grade B");
}
else if (marks >= 40) {
    console.log("Grade C");
}
else {
    console.log("Fail");
}